        public static string GetMimeTypeFromFile(string filePath)
        {
            if (!File.Exists(filePath)) throw new FileNotFoundException(filePath + " not found");

            var buffer = new byte[256];
            using (var fs = new FileStream(filePath, FileMode.Open))
            {
                if (fs.Length >= 256) fs.Read(buffer, 0, 256);
                else fs.Read(buffer, 0, (int)fs.Length);
            }
            try
            {
                uint mimetype;
                FindMimeFromData(0, null, buffer, 256, null, 0, out mimetype, 0);
                var mimeTypePtr = new IntPtr(mimetype);
                var mime = Marshal.PtrToStringUni(mimeTypePtr);
                Marshal.FreeCoTaskMem(mimeTypePtr);
                return mime;
            }
            catch
            {
                return "unknown/unknown";
            }
        }


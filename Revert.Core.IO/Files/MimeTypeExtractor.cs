        public string ConvertMimeTypeByFileExtensionToFileExtensionByMimeType()
        {
            var fileExtensionsByMimeTypeInnerText = new StringBuilder();
            var mimeTypes = new HashSet<string>();

            foreach (var item in mimeTypeByFileExtension)
            {
                if (!mimeTypes.Add(item.Value)) continue;
                if (fileExtensionsByMimeTypeInnerText.Length != 0) fileExtensionsByMimeTypeInnerText.AppendLine(",");
                fileExtensionsByMimeTypeInnerText.Append(string.Format("{{\"{0}\", \"{1}\"}}", item.Value, item.Key));
            }

            return string.Format("private static Dictionary<string, string> fileExtensionByMimeType = new Dictionary<string, string>(){0}{{{0}{1}}};",
                Environment.NewLine, fileExtensionsByMimeTypeInnerText);
        }


